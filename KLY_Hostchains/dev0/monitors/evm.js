//Make it global
let web3=new Web3(global.CONFIG.SYMBIOTE.MONITOR.URL),

//Get the requiered stuff from configs
{ABI,CONTRACT,TICKER} = global.CONFIG.SYMBIOTE.MONITOR,

//Create the contract instance
contractInstance = new web3.eth.Contract(ABI,CONTRACT),




//threadID - VERIFICATION_THREAD | QUORUM_THREAD

GET_CONTRACT_EVENTS_RANGE=async threadID=>{

    /*
    
        global.SYMBIOTE_META[threadID].CHECKPOINT.RANGE_POINTER - <index of array events not to start from 0 each time>
        global.SYMBIOTE_META[threadID].CHECKPOINT.RANGE_FINISH_BLOCK - <id of the latest block in range>
    
    */


    let isInitialLoad = global.SYMBIOTE_META[threadID].CHECKPOINT.RANGE_FINISH_BLOCK === global.SYMBIOTE_META[threadID].CHECKPOINT.RANGE_START_BLOCK,

        nextRangeStartsFrom = isInitialLoad ? global.CONFIG.SYMBIOTE.MONITOR.MONITORING_START_FROM : global.SYMBIOTE_META[threadID].CHECKPOINT.RANGE_FINISH_BLOCK+1,

        wasLatestRangeEmpty = global.SYMBIOTE_META[threadID].CHECKPOINT.RANGE_POINTER === -1,

        weFinishedToEnumThisRange = global.SYMBIOTE_META[threadID].CHECKPOINT.RANGE_POINTER === global.SYMBIOTE_META[threadID+'_EVENTS'].length && global.SYMBIOTE_META[threadID+'_EVENTS'].length !==0



    if(weFinishedToEnumThisRange || isInitialLoad || wasLatestRangeEmpty){

        // We should find next range if no more range exists locally
        // Otherwise - get the range from local storage and add to cache


        let range = await global.SYMBIOTE_META.HOSTCHAIN_DATA.get(nextRangeStartsFrom+`_${threadID}_EVENTS`).catch(_=>false)

        if(range){
            
            global.SYMBIOTE_META[threadID].CHECKPOINT.RANGE_POINTER=0 //reset the counter to the start of array

            global.SYMBIOTE_META[threadID].CHECKPOINT.RANGE_START_BLOCK=nextRangeStartsFrom

            global.SYMBIOTE_META[threadID].CHECKPOINT.RANGE_FINISH_BLOCK=range.latestBlockInRange

            global.SYMBIOTE_META[threadID+'_EVENTS']=range.events

            return global.SYMBIOTE_META[threadID+'_EVENTS']

        }else{

            //Otherwise - query next range
            
            let lastKnownBlockNumber = await web3.eth.getBlockNumber().catch(error=>{

                    LOG(`Some error occured with hostchain node => \x1b[32;1m${error}`,'W')

                    return false

                })

    
            if(lastKnownBlockNumber){

                LOG(`Found new latest known block on hostchain [\x1b[33;1m${TICKER}\x1b[36;1m] => \x1b[32;1m${lastKnownBlockNumber}`,'I')

                //Get from the height we stopped till the last known block
        
                let options = {
    
                    fromBlock:nextRangeStartsFrom,

                    toBlock:lastKnownBlockNumber
    
                };

                //If node works too fast - we shoudn't ask blocks from X+1 to X (coz X<Z+1)
                if(nextRangeStartsFrom>=lastKnownBlockNumber) return
            
                let events = await contractInstance.getPastEvents('Checkpoint',options).catch(error=>{

                    LOG(`Received this error when asking for events => ${error}`,'W')

                    return false

                })

    
                if(Array.isArray(events)){

                    if(events.length){

                        //Update thread data and store locally not to make requests too frequently

                        let range={
                        
                            events,

                            latestBlockInRange:lastKnownBlockNumber

                        }

                        await global.SYMBIOTE_META.HOSTCHAIN_DATA.put(nextRangeStartsFrom+`_${threadID}_EVENTS`,range).catch(
                        
                            error => LOG(`Error occured when trying to store events => ${error}`)
                        
                        )

                        //TODO:And probably - delete the previous range(not to take up much space)

                    }else {

                        //If no events on range [nextRangeStartsFrom;lastKnownBlockNumber] - change the range

                        global.SYMBIOTE_META[threadID].CHECKPOINT.RANGE_POINTER=-1 // -1 means that latest received range has no events 

                        global.SYMBIOTE_META[threadID].CHECKPOINT.RANGE_START_BLOCK=nextRangeStartsFrom

                        global.SYMBIOTE_META[threadID].CHECKPOINT.RANGE_FINISH_BLOCK=lastKnownBlockNumber

                    }

                }

            }

        }

    } else {

        if(!global.SYMBIOTE_META[threadID+'_EVENTS']){

            global.SYMBIOTE_META[threadID+'_EVENTS']=await global.c.get(global.SYMBIOTE_META[threadID].CHECKPOINT.RANGE_START_BLOCK+`_${threadID}_EVENTS`).catch(_=>false)

        }

        return global.SYMBIOTE_META[threadID+'_EVENTS']

    }
        
},




CHECK_IF_AT_LEAST_ONE_DAY_DIFFERENCE=(timestampLater,timestampEarlier)=>{

    let startOfDayLater = new Date(timestampLater),

        startOfDayEarlier = new Date(timestampEarlier)


    startOfDayLater.setUTCHours(0, 0, 0, 0)

    startOfDayEarlier.setUTCHours(0, 0, 0, 0)

    return startOfDayLater-startOfDayEarlier >= 86400000

},




VERIFY_AND_RETURN_CHECKPOINT=async(event,currentCheckpoint,quorumNumber,majority,rootPub)=>{


    if(CHECK_IF_AT_LEAST_ONE_DAY_DIFFERENCE((+event.returnValues.blocktime)*1000),currentCheckpoint.TIMESTAMP){

        //Knowing the quorum, we can step-by-step enumerate events and find the next valid checkpoint

        let {ID,PAYLOAD_HASH,QUORUM_AGGREGATED_SIGNERS_PUBKEY,QUORUM_AGGREGATED_SIGNATURE,AFK_VOTERS} = JSON.parse(event.returnValues.payload)

        //Make sure it's really next
        let isNext = currentCheckpoint.HEADER.ID+1 === ID


        //[+] Aggregated quorum pubkey ==== AGGREGATE(afkVoters,aggregatedPub)
        //[+] QUORUM_SIZE-afkVoters >= QUORUM_SIZE(2/3N+1) (majority)
        //[+] VERIFY(aggregatedPub,aggregatedSigna,hash)

        let signaIsOk = await bls.verifyThresholdSignature(QUORUM_AGGREGATED_SIGNERS_PUBKEY,AFK_VOTERS,rootPub,'STAGE_2'+PAYLOAD_HASH,QUORUM_AGGREGATED_SIGNATURE,quorumNumber-majority).catch(_=>false)

        
        if(isNext && signaIsOk) {

            let validCheckpoint = {

                HEADER:{

                    ID,

                    PAYLOAD_HASH,

                    QUORUM_AGGREGATED_SIGNERS_PUBKEY,

                    QUORUM_AGGREGATED_SIGNATURE,

                    AFK_VOTERS

                },

                //We'll add checkpoint payload once find it

                //Based on PAYLOAD.POOLS_METADATA, we get new quorum for QUORUM_THREAD. For VERIFICATION_THREAD we get the quorum based on own verification process

                TIMESTAMP:(+event.returnValues.blocktime)*1000,

                COMPLETED:false

            }

            /*
        
            Then,find pure PAYLOAD related to hash in header
        
            PAYLOAD is {

                ISSUER

                PREV_CHECKPOINT_PAYLOAD_HASH

                POOLS_METADATA:{},

                OPERATIONS:{},

                OTHER_SYMBIOTES:{}

            }

            To verify: BLAKE3(JSON.stringify(PAYLOAD)) === HASH_IN_HEADER
        
            */

            let initURLs = [global.CONFIG.SYMBIOTE.GET_CHECKPOINT_PAYLOAD_URL,...GET_ALL_KNOWN_PEERS()]

            LOG(`Going to find body to checkpoint \x1b[34;1m${validCheckpoint.HEADER.ID} ### ${validCheckpoint.HEADER.PAYLOAD_HASH}`,'S')

            
            //__________________________________Start the infinite loop to find the body to checkpoint__________________________________
                
            let shouldStopWhile=false

            while(true){
                
                for(let url of initURLs){
                                
                    let checkpointPayload = await fetch(url+'/payload_for_checkpoint/'+PAYLOAD_HASH).then(r=>r.json()).catch(_=>false)

                    if(checkpointPayload && checkpointPayload.PREV_CHECKPOINT_PAYLOAD_HASH === currentCheckpoint.HEADER.PAYLOAD_HASH && BLAKE3(JSON.stringify(checkpointPayload)) === PAYLOAD_HASH){

                        validCheckpoint.PAYLOAD = checkpointPayload

                        //Change flag for outer(while) cycle
                        shouldStopWhile=true

                        //And break this <for> cycle
                        break

                    }

                }

                if(shouldStopWhile) break
            
            }
            
            return validCheckpoint

        }

    }

}


/*

Checkpoint structure

Header:

{
    PAYLOAD_HASH: <32 bytes BLAKE3 HASH OF CHECKPOINT PAYLOAD. Quorum sign this hash>
    
    QUORUM_AGGREGATED_SIGNERS_PUBKEY:<48 bytes BLS AGGREGATED PUBKEY OF POOLS FROM CURRENT QUORUM WHO SIGNED CHECKPOINT>

    QUORUM_AGGREGATED_SIGNATURE:<96 bytes BLS AGGREGATED SIGNATURE which verified by aggregated pubkey>

    AFK_VOTERS:<ARRAY OF AFK POOLS FROM QUORUM WHO SKIPPED CHECKPOINT GENERATION PROCEDURE.48 BYTES PER PUBKEY>

}

If QUORUM SIZE=127 and threshold is 2/3N+1 (required by typical BFT), then count the worst case - when N/3 is AFK, malicious activity or some other problem(poor connection,fault and so on)

N/3=42

Then, the biggest possible checkpoint size is:

32 - checkpoint payload hash.
+
48 - aggregated BLS pubkey of signers
+
96 - aggregated signature
+
48*42
______________________

2192 bytes - the biggest possible checkpoint size in case QUORUM SIZE=127(pools number might be infinite)


-------------------------------------------------------------Checkpoint payload-------------------------------------------------------------

Checkpoint payload contains:

OTHER_CHECKPOINTS (KEY=>VALUE) => (SYMBIOTE_ID=>CHECKPOINT_HEADER) - object with checkpoints from other symbiotes to perform Hivemind activity

POOLS_METADATA - object like this

        {
            '7GPupbq1vtKUgaqVeHiDbEJcxS7sSjwPnbht4eRaDBAEJv8ZKHNCSu2Am3CuWnHjta': {
                index: -1,
                hash: '0123456789abcdef0123456789abcdef0123456789abcdef0123456789abcdef0123456789abcdef0123456789abcdef0123456789abcdef0123456789abcdef',
                isReserve:boolean
            }
    
        }

    Key - BLS pubkey of validator
    Value - object to track progress to verify blocks step-by-step





*/

export default {

    // threadID - global.SYMBIOTE_META.VERIFICATION_THREAD | global.SYMBIOTE_META.QUORUM_THREAD
    //  QUORUM_THREAD - Receive latest valid checkpoint. If checkpoint of today includes our pubkey - then start accept blocks & share commitments due to the POOLS_METADATA state in checkpoint
    /*
    
     Reminder: Checkpoint structure

        Header:

            {
                PAYLOAD_HASH: <32 bytes BLAKE3 HASH OF CHECKPOINT PAYLOAD. Quorum sign this hash>
    
                QUORUM_AGGREGATED_SIGNERS_PUBKEY:<48 bytes BLS AGGREGATED PUBKEY OF POOLS FROM CURRENT QUORUM WHO SIGNED CHECKPOINT>

                QUORUM_AGGREGATED_SIGNATURE:<96 bytes BLS AGGREGATED SIGNATURE which verified by aggregated pubkey>

                AFK_VOTERS:<ARRAY OF AFK POOLS FROM QUORUM WHO SKIPPED CHECKPOINT GENERATION PROCEDURE.48 BYTES PER PUBKEY>

            }

            
        Payload:

            {

                PREV_CHECKPOINT_PAYLOAD_HASH

                POOLS_METADATA:{
                
                    VALIDATOR_0 : {index:number,hash:string,isReserve:boolean},

                    VALIDATOR_1 : {index:number,hash:string,isReserve:boolean},

                    ...

                    VALIDATOR_N : {index:number,hash:string,isReserve:boolean}
                
                }

                OPERATIONS:{

                    Here we add/remove pools, assign to pools and so on

                }

                
                OTHER_SYMBIOTES:{

                    SYMBIOTE_ID_X: CHECKPOINTS_HEADERS_OF_OTHER_SYMBIOTES,

                    SYMBIOTE_ID_Y: CHECKPOINTS_HEADERS_OF_OTHER_SYMBIOTES,

                    ...

                }

            }

        Timestamp:number(we set the timestamp from hostchains to track days changes)

        Completed:bool(required by VERIFICATION_THREAD)



        **************************************************************************************************************

        In checkpoint on hostchain(no matter what hostchain you use) we receive raw version of checkpoint in hexademical format

        First 32 bytes - checkpoint payload hash

        Next 48 bytes - aggregated BLS pubkey of signers(it should be 2/3N+1 of current quorum)

        Next 96 bytes - aggregated signature

        The rest - splitted for 48 bytes BLS pubkeys of members whose signatures we hadn't get(due to network issues or order in cycle)


        => { hash, aggregatedPub, aggregatedSigna, afkVoters }
        
        ***************************************************************************************************************

        To verify checkpoint is valid(so we can accept it and continue verification thread):

            [+] Aggregated quorum pubkey ==== AGGREGATE(afkVoters,aggregatedPub)

            [+] QUORUM_SIZE-afkVoters >= QUORUM_SIZE(2/3N+1)

            [+] VERIFY(aggregatedPub,aggregatedSigna,hash)


    
    */
    
    GET_VALID_CHECKPOINT:async threadID => {


        let currentCheckpoint = global.SYMBIOTE_META[threadID].CHECKPOINT,

            quorumNumber=global.SYMBIOTE_META[threadID].CHECKPOINT.QUORUM.length,

            majority = GET_MAJORITY(threadID),

            checkpointFullID = currentCheckpoint.HEADER.PAYLOAD_HASH+"#"+currentCheckpoint.HEADER.ID,

            rootPub = global.SYMBIOTE_META.STATIC_STUFF_CACHE.get((threadID==='VERIFICATION_THREAD' ? 'VT_ROOTPUB':'QT_ROOTPUB')+checkpointFullID)


        //Find next checkpoint and verify signatures

        let eventsRange = await GET_CONTRACT_EVENTS_RANGE(threadID)


        if(eventsRange){
        
            //Start array with global.SYMBIOTE_META[threadID].CHECKPOINT.RANGE_POINTER(pointer to position in range not to start from 0 position each time)

            let possibleValidCheckpoint,

                startFrom = global.SYMBIOTE_META[threadID].CHECKPOINT.RANGE_POINTER
            

            //Start to enumerate the range of events, starting from position <startFrom>
            
            for(let index in eventsRange){

                if(index<startFrom) continue

                else{

                    possibleValidCheckpoint = await VERIFY_AND_RETURN_CHECKPOINT(eventsRange[index],currentCheckpoint,quorumNumber,majority,rootPub).catch(_=>false)

                    //We do break here, because valid checkpoint was found
                    if(possibleValidCheckpoint?.PAYLOAD) break

                    //If log is not a checkpoint - just increase counter for progress
                    else global.SYMBIOTE_META[threadID].CHECKPOINT.RANGE_POINTER++

    
                }

            }

            if(possibleValidCheckpoint?.PAYLOAD) return possibleValidCheckpoint 

        }

    },


    


    //This function gets the information from hostchain and if it's a new day - we can start proposing the checkpoint
    CHECK_IF_ITS_TIME_TO_PROPOSE_CHECKPOINT:async()=>{

        let latestKnownBlock = await web3.eth.getBlockNumber().catch(_=>false)

        if(latestKnownBlock){

            // We should take previous blocks to make sure that new day has started

            let severalBlocksAgo = global.CONFIG.SYMBIOTE.MONITOR.BLOCK_STOCK_TO_PROPOSE_CHECKPOINT,

                block = await web3.eth.getBlock(latestKnownBlock-severalBlocksAgo).catch(_=>false)


            if(block){

                return CHECK_IF_AT_LEAST_ONE_DAY_DIFFERENCE((+block.timestamp)*1000,global.SYMBIOTE_META.QUORUM_THREAD.CHECKPOINT.TIMESTAMP)

            }

        }

    }
    
}